HAL_StatusTypeDef SETTING_SaveArray(int16_t settingArray[24], uint32_t arrayAddress)
{
	HAL_StatusTypeDef Status = HAL_ERROR;
	
	/* For each member in the setting array */
	for (uint8_t i = 0; i < 24; i ++)
	{
		/* Save setting value */
		Status = HAL_FLASH_Program(FLASH_TYPEPROGRAM_HALFWORD, arrayAddress, settingArray[i]);
		if (Status != HAL_OK) return Status;

		arrayAddress = arrayAddress + 2;
	}

	return Status;
}



HAL_StatusTypeDef SETTING_Save(SETTING_Values *Values)
{
	FLASH_EraseInitTypeDef EraseInit;
	uint32_t PageError;
	HAL_StatusTypeDef Status = HAL_ERROR;
	
	EraseInit.TypeErase = FLASH_TYPEERASE_PAGES;
	EraseInit.Banks = FLASH_BANK_1;
	EraseInit.PageAddress = SETTING_PAGE_ADDRESS;
	EraseInit.NbPages = 1;

	/* Unlock Flash */
	Status = HAL_FLASH_Unlock();
	if (Status != HAL_OK) return Status;

	/* Erase Flash page */
	Status = HAL_FLASHEx_Erase(&EraseInit, &PageError);
	if (Status != HAL_OK) return Status;

	/* Save setting arrays */
	Status = SETTING_SaveArray(Values->SETTING_Array01, SETTING_ARRAY01_ADDRESS);
	if (Status != HAL_OK) return Status;

	Status = SETTING_SaveArray(Values->SETTING_Array02, SETTING_ARRAY02_ADDRESS);
	if (Status != HAL_OK) return Status;

	Status = SETTING_SaveArray(Values->SETTING_Array03, SETTING_ARRAY03_ADDRESS);
	if (Status != HAL_OK) return Status;

	Status = SETTING_SaveArray(Values->SETTING_Array04, SETTING_ARRAY04_ADDRESS);
	if (Status != HAL_OK) return Status;

	Status = SETTING_SaveArray(Values->SETTING_Array05, SETTING_ARRAY05_ADDRESS);
	if (Status != HAL_OK) return Status;

	/* Save code */
	Status = HAL_FLASH_Program(FLASH_TYPEPROGRAM_WORD, SETTING_CODE_ADDRESS, SETTING_CODE);
	if (Status != HAL_OK) return Status;

	/* Lock Flash */
	Status = HAL_FLASH_Lock();

	return Status;
}



